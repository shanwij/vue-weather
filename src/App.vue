<script>

import * as VueGoogleMaps from 'vue2-google-maps'
export default {
  name: 'app',
  data () {
    return {
      api_key: '47fa7ea4ebdfe74d5d5419a3a5bf5bf6',
      url_base: 'https://api.openweathermap.org/data/2.5/',
      query: '',
      weather: {}
    }
  },
  computed: {
    google: VueGoogleMaps.gmapApi
  },

  mounted() {
    new VueGoogleMaps.gmapApi.maps.places.Autocomplete(
      document.getElementById("autocomplete"),
      {
        mounted: new VueGoogleMaps.gmapApi.maps.LatLngBounds(
          new VueGoogleMaps.gmapApi.maps.Lating(45.4215296 , -75.6971931)
        )
      }
    )
  },

  methods: {
    fetchWeather (e) {
      if (e.key == "Enter") {
        fetch(`${this.url_base}weather?q=${this.query}&units=metric&APPID=${this.api_key}`)
          .then(res => {
            return res.json();
          }).then(this.setResults);
      }
      
    },
    submit(){
          fetch(`${this.url_base}weather?q=${this.query}&units=metric&APPID=${this.api_key}`)
          .then(res => {
            return res.json();
          }).then(this.setResults);
    },

    setResults (results) {
      this.weather = results;
    },
    dateBuilder () {
      let d = new Date();
      let months = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"];
      let days = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"];
      let day = days[d.getDay()];
      let date = d.getDate();
      let month = months[d.getMonth()];
      let year = d.getFullYear();
      return `${day} ${date} ${month} ${year}`;
    }
    
  }
}
</script>

